package org.obrii.mit.dp2021.jurilents.domain;

public class Entity {

    private long id;
    private String name;
    private int age;

    public Entity() { }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

}
